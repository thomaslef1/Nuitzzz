Cette Déclaration de Cookies explique comment Nuizzz utilise des cookies et autres technologies similaires pour collecter et stocker des informations lorsque vous visitez notre site web www.nuizzz.com. En utilisant notre site, vous acceptez l’utilisation de cookies conformément à cette Déclaration.

1. Qu’est-ce qu’un Cookie ?

Un cookie est un petit fichier texte déposé sur votre appareil (ordinateur, smartphone, tablette) lorsque vous visitez un site web. Les cookies permettent de mémoriser vos préférences, de faciliter la navigation et de fournir des services personnalisés.

2. Types de Cookies Utilisés

Nous utilisons les types de cookies suivants sur Nuizzz :

Cookies Essentiels : Ces cookies sont nécessaires au bon fonctionnement du site et vous permettent d’utiliser ses principales fonctionnalités, comme l’accès à votre compte. Sans ces cookies, le site ne peut pas fonctionner correctement.
Cookies de Performance : Ces cookies collectent des informations sur la manière dont les visiteurs utilisent notre site, par exemple, les pages les plus visitées et les messages d’erreur. Ces cookies ne collectent pas d’informations qui identifient un visiteur. Toutes les informations collectées sont agrégées et donc anonymes. Ils sont utilisés uniquement pour améliorer le fonctionnement de notre site.
Cookies de Fonctionnalité : Ces cookies permettent au site de se souvenir des choix que vous faites (comme votre nom d’utilisateur, votre langue ou la région où vous vous trouvez) et de fournir des fonctionnalités améliorées et plus personnelles.
Cookies Publicitaires : Ces cookies sont utilisés pour afficher des publicités qui vous sont pertinentes en fonction de vos intérêts. Ils peuvent également être utilisés pour limiter le nombre de fois que vous voyez une publicité et pour aider à mesurer l’efficacité d’une campagne publicitaire.
Cookies de Réseaux Sociaux : Ces cookies sont utilisés pour vous permettre de partager des pages et du contenu que vous trouvez intéressants sur notre site à travers des réseaux sociaux tiers et d’autres sites web.

