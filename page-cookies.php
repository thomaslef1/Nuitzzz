4. Cookies de Tiers

Nous pouvons autoriser des tiers à placer des cookies sur votre appareil pour fournir des services en notre nom. Ces tiers incluent, par exemple, des réseaux publicitaires ou des fournisseurs de services d’analyse. Ces cookies tiers sont soumis aux politiques de confidentialité des prestataires respectifs.

5. Durée de Conservation des Cookies

Les cookies peuvent être conservés sur votre appareil pour différentes périodes. Certains cookies sont supprimés dès que vous fermez votre navigateur (cookies de session), tandis que d’autres restent sur votre appareil jusqu’à ce qu’ils expirent ou que vous les supprimiez (cookies persistants).

